def selection_sort_chars(arr):
    n = len(arr)
    for i in range(n):
        min_idx = i
        for j in range(i + 1, n):
            if arr[j] < arr[min_idx]:
                min_idx = j
        arr[i], arr[min_idx] = arr[min_idx], arr[i]

arr = ['d', 'a', 'c', 'b']
selection_sort_chars(arr)
print(arr)